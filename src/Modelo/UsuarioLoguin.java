
package Modelo;


public class UsuarioLoguin {

    String User;
    String Pass;
    
    public UsuarioLoguin(){
        User="";
        Pass="";
    }

    public String getUser() {
        return User;
    }

    public void setUser(String User) {
        this.User = User;
    }

    public String getPass() {
        return Pass;
    }

    public void setPass(String Pass) {
        this.Pass = Pass;
    }
    
    
    
}
